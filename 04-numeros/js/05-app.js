'use strict';

// Incrementar

let puntuacion = 5;

puntuacion++; // 6
++puntuacion; // 7

puntuacion += 3; // 10

// Decrementar
let puntos = 3;
--puntos; // 2
puntos--; // 1



/************************************************************************************ */
console.log(puntuacion); // INC
console.log(puntos); // DEC