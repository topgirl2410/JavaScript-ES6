'use strict';

// Incrementar

let puntuacion = 5;

puntuacion++; // 6
++puntuacion; // 7

puntuacion += 3; // 10 (suma 3);

// Decrementar
let puntos = 7;
--puntos; // 6
puntos--; // 5
puntos -= 3; // 2 (resta 3);



/************************************************************************************ */
console.log(puntuacion); // INC
console.log(puntos); // DEC