'use strict';

/**
 * ¿Que es un IF?
 * - If es una estructura de control utilizada para tomar decisiones. Es un condicional
 * que sirve para realizar unas u otras operaciones en función de una expresión. Funciona
 * de la siguiente manera, primero se evalua una expresión, si da resultado positivo se 
 * realizan las acciones relacionadas con el caso positivo.
 */

const puntuacion = 1001;

if (puntuacion == 1000) {
    console.log("si es el mismo");
} else {
    console.log("no, no se cumple");
}
