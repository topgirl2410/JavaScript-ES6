'use strict';

/**************************************(Operador mayor que...)*********************************************** */

const money = 500;
const gastos = 300;


if (money >= gastos) {
    console.log("Tarjeta aceptada")
} else {
    console.log("Error en el pago")
}

/*********************************************(Operador menor que...)********************************* */
const dinero = 200;
const totalAPagar = 450;

if (dinero <= totalAPagar) {
    console.log("Error en el pago")
} else {
    console.log("Tarjeta aceptada")
}
